static const u8 solid_tiles[] = { TILE_WALL, TILE_WATER, TILE_TREE };

void map_init(void) {
    int x, y;
    u16 *sb;

    /* Load palettes + tile graphics into VRAM */
    tiles_init();

    /* BG0: 4bpp, charblock 0, screenblock 28, 32×32 tiles */
    REG_BG0CNT = BG_MAP_BASE(28) | BG_TILE_BASE(0) | BG_SIZE_0; /* 4bpp is default (0) */

    /* Fill screenblock 28 with town_map tile indices */
    sb = (u16 *)(VRAM + 0xE000); /* screenblock 28 = VRAM + 28*0x800 */
    for (y = 0; y < MAP_ROWS; y++) {
        for (x = 0; x < MAP_COLS; x++) {
            sb[y * 32 + x] = (u16)town_map[y][x];
        }
    }

    /* Hide all sprites */
    oam_clear();
}

void map_draw(void) {
    int sx = player.px - (SCREEN_W / 2) + (TILE_SIZE / 2);
    int sy = player.py - (SCREEN_H / 2) + (TILE_SIZE / 2);
    if (sx < 0) sx = 0;
    if (sy < 0) sy = 0;
    REG_BG0HOFS = sx;
    REG_BG0VOFS = sy;
}

int map_is_solid(s16 x, s16 y) {
    if (x < 0 || y < 0 || x >= MAP_COLS || y >= MAP_ROWS) return 1;
    u8 tile = town_map[y][x];
    for (int i = 0; i < (int)sizeof(solid_tiles); i++) {
        if (tile == solid_tiles[i]) return 1;
    }
    return 0;
}
